function toHtmlEntity(dec: number): string {
  if (NAMED_ENTITIES[dec]) return NAMED_ENTITIES[dec]
  return `&#${dec};`
}

export function buildAsciiTable(): AsciiEntry[] {
  const entries: AsciiEntry[] = []
  for (let dec = 0; dec <= 255; dec++) {
    const isC0 = dec < 32
    const isC1 = dec >= 128 && dec <= 159
    const isControl = isC0 || dec === 127 || isC1
    const char = isControl ? (CONTROL_NAMES[dec] ?? '') : String.fromCharCode(dec)
    const description = CONTROL_DESCRIPTIONS[dec] ?? String.fromCharCode(dec)
    entries.push({
      dec,
      hex: dec.toString(16).toUpperCase().padStart(2, '0'),
      oct: dec.toString(8).padStart(3, '0'),
      bin: dec.toString(2).padStart(8, '0'),
      char,
      htmlEntity: toHtmlEntity(dec),
      description,
    })
  }
  return entries
}
